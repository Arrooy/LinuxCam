struct TextSize
{
    int width;
    int height;
};

// Compute text dimensions (in pixels) for a given scale
inline TextSize getTextSize(const std::string& text, int scale = 1)
{
    return {static_cast<int>(text.size()) * 8 * scale, 8 * scale};
}

// Draw a single scaled pixel block using DDA to fill
inline void fillBlockWithDDA(Image& img, int x1, int y1, int size, const Pixel& color)
{
    for (int dy = 0; dy < size; dy++)
    {
        auto line = math_utils::DDA<long>(x1, y1 + dy, x1 + size - 1, y1 + dy);
        img.paintPoints(line, color);
    }
}


// Draw a single character scaled by `scale` using DDA
inline void drawCharDDA(Image& img, int x, int y, char c, const Pixel& color, int scale = 1)
{
    if (c < 0 || c > 127)
    {
        return;
    }
    const unsigned char* glyph = font8x8_basic[(int) c];
    for (int row = 0; row < 8; row++)
    {
        uint8_t bits = glyph[row];
        for (int col = 0; col < 8; col++)
        {
            if (bits & (1 << col))
            {
                fillBlockWithDDA(img, x + col * scale, y + row * scale, scale, color);
            }
        }
    }
}


// Draw text onto the Image, with optional centering and scaling via DDA
inline void
drawText(Image& img, int x, int y, const std::string& text, const Pixel& color, int scale = 1, bool center = false)
{
    if (scale <= 0)
    {
        return;
    }
    TextSize ts = getTextSize(text, scale);
    int drawX = center ? x - ts.width / 2 : x;
    int drawY = center ? y - ts.height / 2 : y;
    int cursorX = drawX;
    for (char c : text)
    {
        drawCharDDA(img, cursorX, drawY, c, color, scale);
        cursorX += 8 * scale;
    }
}

// Enhanced API functions for better text rendering capabilities

// Draw text with a background rectangle for better visibility
inline void drawTextWithBackground(Image& img, int x, int y, const std::string& text, const Pixel& textColor,
                                   const Pixel& bgColor, int scale = 1, bool center = false, int padding = 2)
{
    if (scale <= 0 || text.empty())
    {
        return;
    }

    TextSize ts = getTextSize(text, scale);
    int drawX = center ? x - ts.width / 2 : x;
    int drawY = center ? y - ts.height / 2 : y;

    // Draw background rectangle with padding
    int bgX = drawX - padding;
    int bgY = drawY - padding;
    int bgWidth = ts.width + 2 * padding;
    int bgHeight = ts.height + 2 * padding;

    // Fill background using optimized rectangle fill
    img.fillRect(bgX, bgY, bgWidth, bgHeight, bgColor);

    // Draw text on top
    drawText(img, drawX, drawY, text, textColor, scale, false);
}

// Draw multiline text with automatic line breaks
inline void drawMultilineText(Image& img, int x, int y, const std::string& text, const Pixel& color, int scale = 1,
                              int lineSpacing = 2)
{
    if (scale <= 0 || text.empty())
    {
        return;
    }

    std::vector<std::string> lines;
    std::stringstream ss(text);
    std::string line;

    // Split by newlines
    while (std::getline(ss, line, '\n'))
    {
        lines.push_back(line);
    }

    int currentY = y;
    int lineHeight = 8 * scale + lineSpacing;

    for (const auto& textLine : lines)
    {
        drawText(img, x, currentY, textLine, color, scale, false);
        currentY += lineHeight;
    }
}

// Draw text aligned within a bounding rectangle
enum class TextAlignment
{
    LEFT,
    CENTER,
    RIGHT,
    TOP,
    MIDDLE,
    BOTTOM
};

inline void drawTextAligned(Image& img, int rectX, int rectY, int rectWidth, int rectHeight, const std::string& text,
                            const Pixel& color, int scale = 1, TextAlignment hAlign = TextAlignment::CENTER,
                            TextAlignment vAlign = TextAlignment::MIDDLE)
{
    if (scale <= 0 || text.empty() || rectWidth <= 0 || rectHeight <= 0)
    {
        return;
    }

    TextSize ts = getTextSize(text, scale);

    int textX = rectX;
    switch (hAlign)
    {
        case TextAlignment::LEFT:
            textX = rectX;
            break;
        case TextAlignment::CENTER:
            textX = rectX + (rectWidth - ts.width) / 2;
            break;
        case TextAlignment::RIGHT:
            textX = rectX + rectWidth - ts.width;
            break;
        default:
            textX = rectX;
            break;
    }

    int textY = rectY;
    switch (vAlign)
    {
        case TextAlignment::TOP:
            textY = rectY;
            break;
        case TextAlignment::MIDDLE:
            textY = rectY + (rectHeight - ts.height) / 2;
            break;
        case TextAlignment::BOTTOM:
            textY = rectY + rectHeight - ts.height;
            break;
        default:
            textY = rectY;
            break;
    }

    drawText(img, textX, textY, text, color, scale, false);
}

// Check if text fits within given dimensions
inline bool textFitsInRect(const std::string& text, int scale, int maxWidth, int maxHeight)
{
    if (text.empty() || scale <= 0 || maxWidth <= 0 || maxHeight <= 0)
    {
        return false;
    }

    TextSize ts = getTextSize(text, scale);
    return ts.width <= maxWidth && ts.height <= maxHeight;
}

// Find the largest scale that fits text within given dimensions
inline int findMaxScaleForRect(const std::string& text, int maxWidth, int maxHeight, int maxScale = 10)
{
    if (text.empty() || maxWidth <= 0 || maxHeight <= 0)
    {
        return 0;
    }

    for (int scale = maxScale; scale >= 1; scale--)
    {
        if (textFitsInRect(text, scale, maxWidth, maxHeight))
        {
            return scale;
        }
    }
    return 0;
}

// Validate character is renderable (within font range)
inline bool isCharacterRenderable(char c)
{
    return c >= 0 && c <= 127;
}

// Count renderable characters in text
inline size_t countRenderableCharacters(const std::string& text)
{
    size_t count = 0;
    for (char c : text)
    {
        if (isCharacterRenderable(c))
        {
            count++;
        }
    }
    return count;
}

// Get font glyph data for a character (for testing/debugging)
inline const unsigned char* getFontGlyph(char c)
{
    if (!isCharacterRenderable(c))
    {
        return nullptr;
    }
    return font8x8_basic[static_cast<int>(c)];
}

